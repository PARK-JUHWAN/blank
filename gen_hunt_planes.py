def evaluate(kind, openness, note):
    if kind == "X":
        return None, f"탈락 [{note}]"
    if kind == "W":
        return 4, f"4/6 (broken {note})"
    if kind == "P":
        return 5, f"5/6 (broken {note})"
    # L
    if openness == "E":
        return 6, f"6/6 ✅✅✅ (fragile: {note} — 자본 아님, 채택레이스)"
    if openness == "C":
        return 5, "5/6 (broken 3:락인 경합=주목)"
    return 4, "4/6 (broken 5:인큐번트 점유)"

